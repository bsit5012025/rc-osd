package org.rocs.osd.model.person.guardian;

/**
 * Enum representing the type of relationship a guardian has with a student.
 */
public enum Relationship {
    /** Indicates the guardian is the student's father. */
    FATHER,
    /** Indicates the guardian is the student's mother. */
    MOTHER,
    /** Indicates the guardian is another person responsible for the student. */
    GUARDIAN
}
